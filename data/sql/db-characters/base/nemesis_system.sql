CREATE TABLE IF NOT EXISTS `character_nemesis` (
    `guid` bigint unsigned NOT NULL COMMENT 'Creature spawn id',
    `creature_entry` int unsigned NOT NULL COMMENT 'Original creature entry',
    `map_id` int unsigned NOT NULL,
    `pos_x` float NOT NULL,
    `pos_y` float NOT NULL,
    `pos_z` float NOT NULL,
    `rank` tinyint unsigned NOT NULL DEFAULT 1 COMMENT 'Current nemesis rank',
    `affix_mask` int unsigned NOT NULL DEFAULT 0 COMMENT 'Reserved for future affixes',
    `base_health` int unsigned NOT NULL COMMENT 'Creature base health before nemesis scaling',
    `base_scale` float NOT NULL COMMENT 'Creature native scale before nemesis scaling',
    `base_melee_min_damage` float NOT NULL COMMENT 'Base melee min damage before nemesis scaling',
    `base_melee_max_damage` float NOT NULL COMMENT 'Base melee max damage before nemesis scaling',
    `base_ranged_min_damage` float NOT NULL COMMENT 'Base ranged min damage before nemesis scaling',
    `base_ranged_max_damage` float NOT NULL COMMENT 'Base ranged max damage before nemesis scaling',
    `base_attack_time` int unsigned NOT NULL COMMENT 'Base melee attack time before affix scaling',
    `base_range_attack_time` int unsigned NOT NULL COMMENT 'Base ranged attack time before affix scaling',
    `base_run_speed_rate` float NOT NULL COMMENT 'Base run speed rate before affix scaling',
    `nemesis_target_guid` int unsigned NOT NULL COMMENT 'Player guid that created the nemesis',
    `last_promotion_at` int unsigned NOT NULL DEFAULT 0 COMMENT 'Last nemesis promotion time as unix timestamp',
    `last_victim_guid` int unsigned NOT NULL DEFAULT 0 COMMENT 'Last victim guid used for anti-feed cooldowns',
    `creation_date` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (`guid`),
    KEY `idx_character_nemesis_target` (`nemesis_target_guid`),
    KEY `idx_character_nemesis_created` (`creation_date`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;